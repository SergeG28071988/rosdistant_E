# Напишите программу, которая создает словарь, заполняет его случайными элементами и сортирует его по значениям.

import random

try:
    # Создаем пустой словарь
    my_dict = {}

    # Запрашиваем у пользователя количество элементов в словаре
    n = int(input("Введите количество элементов в словаре: "))

    # Заполняем словарь случайными элементами
    for i in range(n):
        key = random.randint(1, 100)
        value = random.randint(1, 100)
        my_dict[key] = value

    # Выводим исходный словарь
    print("Исходный словарь:")
    print(my_dict)

    # Сортируем словарь по значениям
    sorted_dict = dict(sorted(my_dict.items(), key=lambda x: x[1]))

    # Выводим отсортированный словарь
    print("Отсортированный словарь:")
    print(sorted_dict)

except ValueError:
    print("Ошибка! Введите целое число.")
